// Usefull in redux reducers
const month = 3;
switch (month) {
  case 1:
    console.log("January");
    break;
  case 2:
    console.log("february");
    break;
  case 3:
    console.log("March");
    break;
  case 4:
    console.log("April");
    break;

  default:
    console.log("Month not found");
    break;
}
